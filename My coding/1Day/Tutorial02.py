print("10 + 5 = ",10 + 5)
print("10 - 3 = ",10 - 3)
print("5 X 5 = ",5*5)
print("14 / 3 = ", 14/3)
print("2^4 = ",2**4)
print("14 // 3 = ",14//3)#나누었을때 몫(소숫점 제외) 출력
print("14 % 3 = ",14 % 3)#나누었을때 나머지 출력
print("2 * 3 + 5 = ",2*3+5)
print("2 * (3 + 5) = ",2*(3+5))#소괄호 안에 있는 연산식을 우선적으로 수행
#10은 2로 나누어 떨어지기 때문에 0이 나온다 즉 10은 2의배수(짝수)
print("10 % 2 = ",10 % 2)
#13은 2로 나누어 떨어지지 않기 때문에 1이 나온다 즉 13 은 2의 배수가 아님(홀수)
print("13 % 2 = ",13 % 2)